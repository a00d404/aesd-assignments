#!/bin/sh

# Проверяем, что переданы оба аргумента
if [ $# -ne 2 ]; then
    echo "Ошибка: Необходимо указать два аргумента: [путь_к_папке] [строка_для_поиска]"
    exit 1
fi

filesdir=$1
searchstr=$2

# Проверяем, существует ли указанная директория
if [ ! -d "$filesdir" ]; then
    echo "Ошибка: Директория '$filesdir' не существует в файловой системе"
    exit 1
fi

# Считаем количество файлов в директории и поддиректориях
# -type f находит только файлы, а wc -l считает их количество
X=$(find "$filesdir" -type f | wc -l)

# Считаем количество строк, содержащих искомую строку searchstr
# -r ищет рекурсивно, -o выводит совпадения (чтобы корректно посчитать строки)
Y=$(grep -r "$searchstr" "$filesdir" | wc -l)

# Выводим финальную строку строго по шаблону из задания
echo "The number of files are $X and the number of matching lines are $Y"
