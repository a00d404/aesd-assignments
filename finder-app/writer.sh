#!/bin/sh

# Проверяем, что переданы оба аргумента
if [ $# -ne 2 ]; then
    echo "Ошибка: Необходимо указать два аргумента: [путь_к_файлу] [текст_для_записи]"
    exit 1
fi

writefile=$1
writestr=$2

# Извлекаем путь к папке, в которой должен лежать файл
# (например, из /tmp/aesd/file.txt получим /tmp/aesd)
target_dir=$(dirname "$writefile")

# Создаем директорию, если её ещё нет (флаг -p создаст всю цепочку папок)
mkdir -p "$target_dir"

# Пытаемся записать строку в файл (перезаписывая его)
echo "$writestr" > "$writefile"

# Проверяем, прошла ли запись успешно (проверяем код возврата последней команды)
if [ $? -ne 0 ]; then
    echo "Ошибка: Не удалось создать или записать в файл '$writefile'"
    exit 1
fi

